#include <stdio.h>

int main()
{
	printf("a对应的ASCII值 %d\n",(int)'a');
	printf("z对应的ASCII值 %d\n",(int)'z');
	printf("A对应的ASCII值 %d\n",(int)'A');
	printf("Z对应的ASCII值 %d\n",(int)'Z');
	printf("0对应的ASCII值 %d\n",(int)'0');
	printf(" 对应的ASCII值 %d\n",(int)' ');

	printf("S T A R T对应的ASCII值 0x%x 0x%x 0x%x 0x%x 0x%x\n",(int)'S',(int)'T',(int)'A',(int)'R',(int)'T');
	return 0;
}
